#include <stdio.h>

int main() {
    int x = 10; // initialize x with value 10
    printf("The value of x is %d\n", x); // print x to the console
    for (int i = 0; i < x; i++) { // loop from 0 to x
        printf("The current value of i is %d\n", i); // print i to the console
    }
    return 0; // exit the program with success
}
